use Doctrine\ORM\Mapping as ORM;

/**
 * Plain Doctrine ORM entity (NOT marked #[ApiResource]).
 * Used to test that nested property filters work on relations to non-ApiResource entities.
 *
 * @see https://github.com/api-platform/core/issues/7916
 */
#[ORM\Entity]
#[ORM\Table(name: 'issue_7916_user')]
class User
{
    #[ORM\Id]
    #[ORM\GeneratedValue]
    #[ORM\Column(type: 'integer')]
    private ?int $id = null;

    #[ORM\Column(type: 'string', length: 255)]
    private ?string $name = null;

    #[ORM\Column(type: 'string', length: 255)]
    private ?string $email = null;

    public function getId(): ?int
    {
        return $this->id;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(?string $name): self
    {
        $this->name = $name;

        return $this;
    }

    public function getEmail(): ?string
    {
        return $this->email;
    }

    public function setEmail(?string $email): self
    {
        $this->email = $email;

        return $this;
    }
}
